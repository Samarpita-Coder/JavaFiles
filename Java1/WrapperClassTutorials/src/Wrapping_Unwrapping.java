
public class Wrapping_Unwrapping {

	public static void main(String[] args) {
		byte a = 1;
		  
        // wrapping around Byte object
        Byte byteobj = new Byte(a);
        
     // unwrapping objects to primitive data types
        byte bv = byteobj;
        System.out.println(bv);
        

	}

}
